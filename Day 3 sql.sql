drop table if exists #Temp
select 
	a.s,
	a.id,
	b.x,b.y,
	ROW_NUMBER() over(partition by b.y, b.x order by id) as rn
into #Temp
from #test a
inner join #slopes b on (a.id % b.y = 0) or id = 1

select 
	sum(
		case when 
			substring(s, 
						case when ((rn * a.x) - (a.x - 1)) % len(s) = 0 then 31
						else ((rn * a.x) - (a.x - 1)) % len(s) end 
						,1) 
			= '#' then 1 
		else 0 end
	) as treeSum,
	a.x,a.y
from #Temp a
group by 
a.x,a.y







